#Numeric Data Types
a = 13
b = 100
c = -66
print(a,b,c)
print(type(a),type(b),type(c))

#Floating Point Data Types
x = 33.5
y = -25.8
z = 205
print(x,y,z)
print(type(x),type(y),type(z))

#Complex Data Types
comp = 3+5j
print(type(comp))

#Binary Data Types
binaryliteral = 0B1010
print(binaryliteral)
print(type(binaryliteral))

#HexaDecimal Data Type
hexadecimalliteral = 0Xff
print(hexadecimalliteral)
print(type(hexadecimalliteral))

#Octal Data type
octalliteral = 0O11
print(octalliteral)
print(type(octalliteral))

#Boolean Data Types
booleanliteral = True
print(booleanliteral)
print(type(booleanliteral))
print(9>8)

#Type Conversions
d = int(x)          #Converts a Number or a String to Integer
print(d)
print(type(d))
